export type ElementCategory = 
  | 'diatomic-nonmetal'
  | 'noble-gas'
  | 'alkali-metal'
  | 'alkaline-earth-metal'
  | 'metalloid'
  | 'polyatomic-nonmetal'
  | 'post-transition-metal'
  | 'transition-metal'
  | 'lanthanide'
  | 'actinide'
  | 'halogen'
  | 'nonmetal'

export interface ChemistryElement {
  atomicNumber: number
  symbol: string
  name: string
  atomicMass: number
  category: ElementCategory
  mainCategory: 'metal' | 'non-metal' | 'metalloid'
  group: number // 1-18
  period: number // 1-7
  electronConfig: string
  funFact: string
  uses: string
  physicalState: 'solid' | 'liquid' | 'gas' | 'unknown'
}

export const getMainCategory = (category: ElementCategory): 'metal' | 'non-metal' | 'metalloid' => {
  if (category === 'metalloid') return 'metalloid'
  if ([
    'diatomic-nonmetal',
    'polyatomic-nonmetal',
    'noble-gas',
    'halogen',
    'nonmetal'
  ].includes(category)) return 'non-metal'
  return 'metal'
}

